class Address:
    def __init__(self, name1: str, name2: str, name3: str, country_code: str, zip_code: str,
                 city: str, street: str, street_number: int, email: str, contact_person: str,
                 fixed_line_phone_number: str, mobile_phone_number: str):
        self.Name1 = name1
        self.Name2 = name2
        self.Name3 = name3
        self.CountryCode = country_code
        self.ZIPCode = zip_code
        self.City = city
        self.Street = street
        self.StreetNumber = street_number
        self.eMail = email
        self.ContactPerson = contact_person
        self.MobilePhoneNumber = mobile_phone_number
        self.FixedLinePhonenumber = fixed_line_phone_number

